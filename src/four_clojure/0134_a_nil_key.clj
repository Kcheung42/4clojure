#_(
   Write a function which, given a key and map, returns true iff the map
   contains an entry with that key and its value is nil.
   )


;; -----------------------------------------------------------------------------
;; Solutions
;; -----------------------------------------------------------------------------

(defn f
  [key map]
  (if (contains? map key)
    (nil? (key map))
    false)
  )

;; -----------------------------------------------------------------------------
;; Tests
;; -----------------------------------------------------------------------------

(every? true?
  (vector
    (true?  (f :a {:a nil :b 2}))
    (false? (f :b {:a nil :b 2}))
    (false? (f :c {:a nil :b 2}))
    ))
