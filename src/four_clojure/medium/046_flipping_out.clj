(ns four-clojure.flipping-out)

;; -----------------------------------------------------------------------------
;; Question
;; -----------------------------------------------------------------------------
;; tags: #medium

#_(Write a higher-order function which flips the order of the arguments of an
         input function.)


;; -----------------------------------------------------------------------------
;; Solutions
;; -----------------------------------------------------------------------------


(defn f
  [f]
  (fn [arg1 arg2]
    (f arg2 arg1)))

;; -----------------------------------------------------------------------------
;; Tests
;; -----------------------------------------------------------------------------

(every? true?
        (vector
         (= 3 ((f nth) 2 [1 2 3 4 5]))
         (= true ((f >) 7 8))
         (= 4 ((f quot) 2 8))
         (= [1 2 3] ((f take) [1 2 3 4 5] 3))))
